    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        ItensRevisao ir = itensrevisao.get(jTable1.getSelectedRow());
        int flag = 0;
        if (jComboBox1.getSelectedIndex() != ir.getItem().getEstado() - 1) {
            ir.getItem().setEstado(jComboBox1.getSelectedIndex() + 1);
            ir.setItem((Item) IDAO.alterar(ir.getItem()));
            ir.setEstado(ir.getItem().getEstado());
            flag++;
        }

        if (jRadioButton1.isSelected()) {
            if (!"ENCONTRADO".equals(ir.getVerificacao())) {
                ir.setVerificacao("ENCONTRADO");
                flag++;

            }
        } else if (jRadioButton2.isSelected()) {
            if (!"NÃO ENCONTRADO".equals(ir.getVerificacao())) {
                ir.setVerificacao("NÃO ENCONTRADO");
                flag++;

            }
        } else if (jRadioButton3.isSelected()) {
            if (!"NÃO ENCONTRADO PAGO".equals(ir.getVerificacao())) {
                ir.setVerificacao("NÃO ENCONTRADO PAGO");
                MovimentacaoItem mi = new MovimentacaoItem();
                mi.setData_criacao(LocalDate.now());
                mi.setData_modificacao(LocalDate.now());
                mi.setItem(ir.getItem());
                mi.setMotivo("ITEM NÃO ENCONTRADO E PAGO.\nMOVIDO PARA O AMBIENTE VIRTUAL DE ITENS PAGOS.");
                mi.setOrigem(ir.getItem().getAmbiente());
                ir.getItem().getAmbiente().setData_modificacao(LocalDate.now());
                ADAO.alterar(ir.getItem().getAmbiente());
                ir.getItem().setData_modificacao(LocalDate.now());
                mi.setDestino(itenspagos);
                ir.getItem().setDono(itenspagos.getServidor());
                ir.getItem().setAmbiente(itenspagos);
                ir.getItem().getAmbiente().setData_modificacao(LocalDate.now());
                ADAO.alterar(ir.getItem().getAmbiente());
                IDAO.alterar(ir.getItem());
                MIDAO.adiciona(mi);
                flag++;

            }
        }
        if (flag != 0) {
            ir.setData_modificacao(LocalDate.now());
            IRDAO.alterar(ir);
        }

        irmodel.fireTableDataChanged();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int flag = 0;
        for (ItensRevisao ir : itensrevisao) {
            if (ir.getVerificacao().equals("NÃO ENCONTRADO")) {
                flag++;
            }
        }
        if (flag == 0) {
            int opc = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja finalizar? "
                    + "Não será possível modificar mais nada.", "Deseja Finalizar?", JOptionPane.OK_OPTION);
            if (opc == JOptionPane.OK_OPTION) {

                ra.setEstado("ENCERRADA");
                RADAO.alterar(ra);
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Algum dos itens da revisão não foi encontrado e não foi pago.", "Impossível Finalizar.", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

