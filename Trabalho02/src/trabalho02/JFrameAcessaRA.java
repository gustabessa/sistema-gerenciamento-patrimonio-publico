    public JFrameAcessaRA(RevisaoAnual ra) {

        initComponents();
        setLocationRelativeTo(null);
        setModal(true);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        itensrevisao = IRDAO.filtrar(ra.getId());
        String dados[] = new String[3];
        this.ra = ra;
        irmodel = (DefaultTableModel) jTable1.getModel();
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_NEXT_COLUMN);
        TableColumnModel colModel = jTable1.getColumnModel();
        colModel.getColumn(0).setPreferredWidth(20);
        colModel.getColumn(1).setPreferredWidth(130);

        srev.setText(ra.getServidor().getNome());
        arev.setText(ra.getAmbiente().getDescricao());
        drev.setText(ra.getData_criacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        mrev.setText(ra.getData_modificacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        jTable1.setModel(irmodel);
        for (ItensRevisao ir : itensrevisao) {
            if (ir.getRevisao().getId() == ra.getId()) {
                dados[0] = String.valueOf(ir.getId());
                dados[1] = ir.getItem().getEspecificacao();
                dados[2] = String.valueOf(ir.getItem().getCod_partimonio());
                irmodel.addRow(dados);
            }
        }
        irmodel.fireTableDataChanged();
        
        for(Ambiente a : ambientes) {
            if(a.getDescricao().equals("ITENS PAGOS"))
                itenspagos = a;
        }
    }

