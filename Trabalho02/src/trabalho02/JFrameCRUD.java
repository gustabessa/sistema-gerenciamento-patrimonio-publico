    private void iupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iupdateActionPerformed
        // TODO add your handling code here:
        String scod = ilist.getSelectedValue();
        int iend = scod.indexOf(" ");
        if (iend != -1) {
            scod = scod.substring(0, iend);
            int cod = Integer.parseInt(scod);
            Item i = IDAO.buscaPorId(cod);
            new JFrameAlterarItem(i).setVisible(true);
            atualizaLista(model4, IDAO);
            List<Object> Itens = IDAO.listar();

        }
    }//GEN-LAST:event_iupdateActionPerformed

    private void ifilterKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ifilterKeyReleased
        // TODO add your handling code here:
        if (ifilter.getText().isEmpty()) {
            model4.removeAllElements();
            for (Item i : IDAO.filtrar(ifilter.getText())) {
                model4.addElement(i.toString());
            }
        }


    }//GEN-LAST:event_ifilterKeyReleased

    private void icfilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_icfilterActionPerformed
        // TODO add your handling code here:
        if (icfilter.isFocusOwner()) {
            ifilter.setText("");
            isfilter.setSelectedIndex(0);
            iafilter.setSelectedIndex(0);
            if (icfilter.getSelectedIndex() != 0) {
                Campus c = (Campus) icfilter.getSelectedItem();
                model4.removeAllElements();
                for (Object i : Itens) {
                    Item i1 = (Item) i;
                    if (i1.getAmbiente().getCampus().getId() == c.getId()) {
                        model4.addElement(i1.toString());
                    }
                }
            } else {
                atualizaLista(model4, IDAO);
            }
        }

    }//GEN-LAST:event_icfilterActionPerformed

