    public void atualizaLista(DefaultListModel model, DAO DAO) {
        model.removeAllElements();
        List<Object> Lista = DAO.listar();
        for (Object o : Lista) {
            model.addElement(o.toString());
        }
    }

    public void atualizaCBox(JComboBox cbox, DAO DAO) {
        List<Object> Lista = DAO.listar();
        int tam = cbox.getItemCount()-1;
        for (int i = 0; i < tam; i++) {
            cbox.removeItemAt(1);
        }
        for (Object o : Lista) {
            cbox.addItem(o);
        }
    }

