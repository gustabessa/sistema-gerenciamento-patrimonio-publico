    private void ilistValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ilistValueChanged
        // TODO add your handling code here:
        if (!ilist.isSelectionEmpty()) {
            String scod = ilist.getSelectedValue();
            int iend = scod.indexOf(" ");
            if (iend != -1) {
                scod = scod.substring(0, iend);
                int cod = Integer.parseInt(scod);
                Item i = IDAO.buscaPorId(cod);
                iid.setText(String.valueOf(i.getId()));
                iespecificacao.setText(i.getEspecificacao());
                switch (i.getEstado()) {
                    case 1:
                        iestado.setText("NOVO");
                        break;
                    case 2:
                        iestado.setText("BOM");
                        break;
                    case 3:
                        iestado.setText("REGULAR");
                        break;
                    case 4:
                        iestado.setText("PRECÁRIO");
                        break;
                    case 5:
                        iestado.setText("OCIOSO");
                        break;
                    case 6:
                        iestado.setText("RECUPERÁVEL");
                        break;
                    case 7:
                        iestado.setText("ANTIECONÔMICO");
                        break;
                    case 8:
                        iestado.setText("IRRECUPERÁVEL");
                        break;
                    default:
                        iestado.setText("NÃO DEFINIDO");
                        break;
                }

                iambiente.setText(i.getAmbiente().getDescricao());
                icampus.setText(i.getAmbiente().getCampus().getAbreviacao());
                icodpatrimonio.setText(String.valueOf(i.getCod_partimonio()));
                ivalorcompra.setText("R$" + String.format("%.2f", i.getValor_compra()));
                idono.setText(i.getDono().getNome());
                idatacompra.setText(i.getData_compra().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                idatacriacao.setText(i.getData_criacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                idatamodificacao.setText(i.getData_modificacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                if (logado.getPapel().equals("adm")) {

                    idelete.setEnabled(true);
                    iupdate.setEnabled(true);
                }
            }
        } else {
            iid.setText("");
            iespecificacao.setText("");
            iestado.setText("");
            iambiente.setText("");
            icampus.setText("");
            icodpatrimonio.setText("");
            ivalorcompra.setText("");
            idono.setText("");
            idatacompra.setText("");
            idatacriacao.setText("");
            idatamodificacao.setText("");
            if (slist.isSelectionEmpty()) {
                idelete.setEnabled(false);
                iupdate.setEnabled(false);
            }
        }
    }//GEN-LAST:event_ilistValueChanged

