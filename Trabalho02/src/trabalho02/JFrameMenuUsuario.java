        jList1.setModel(amodel);
        for (Object a1 : ambientes) {
            Ambiente a = (Ambiente) a1;
            if (a.getServidor().getId() == logado.getId()) {
                amodel.addElement(a.toString());
            }
        }
        for (Object c : campus) {
            ucampus.addItem(c);
        }
        jButton1.setEnabled(false);
        unome.setText(logado.getNome());
        umail.setText(logado.getEmail());
        ucargo.setText(logado.getCargo());
        for (int i = 0; i < ucampus.getItemCount(); i++) {
            if (logado.getCampus().toString().equals(ucampus.getItemAt(i).toString())) {
                ucampus.setSelectedIndex(i);
            }
        }
        if (logado.getPapel().equals("comum")) {
            ucomum.setSelected(true);
        } else {
            uadm.setSelected(true);
        }
        ulogin.setText(logado.getLogin());
        usenha.setText("");
