    private void usaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usaveActionPerformed
        // TODO add your handling code here:
        if (String.valueOf(usenha.getPassword()).isEmpty() || !criptografa(String.valueOf(usenha.getPassword())).equals(logado.getSenha())) {
            JOptionPane.showMessageDialog(this, "Senha vazia ou incorreta.", "ERRO", JOptionPane.OK_OPTION);
            usenha.requestFocus();
        } else {
            logado.setNome(unome.getText());
            logado.setEmail(umail.getText());
            Campus c = (Campus) ucampus.getSelectedItem();
            logado.setCampus(c);
            logado.setCargo(ucargo.getText());
            logado.setLogin(ulogin.getText());
            if (!String.valueOf(nsenha.getPassword()).isEmpty()) {
                logado.setSenha(this.criptografa(String.valueOf(nsenha.getPassword())));
            }
            logado = (Servidor) SDAO.alterar(logado);
            dispose();
            new JFrameMenuUsuario(logado).setVisible(true);
        }

    }//GEN-LAST:event_usaveActionPerformed

