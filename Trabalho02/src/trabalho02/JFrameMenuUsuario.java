    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        if (!jList1.isSelectionEmpty()) {
            String ccod = jList1.getSelectedValue();
            int iend = ccod.indexOf(" ");
            if (iend != -1) {
                ccod = ccod.substring(0, iend);
                int cod = Integer.parseInt(ccod);
                Ambiente a = ADAO.buscaPorId(cod);
                jButton1.setEnabled(true);
                jTextField1.setText(a.getDescricao());
                jTextField2.setText(a.getCampus().getAbreviacao());
                double valora = 0;
                for (Object i1 : itens) {
                    Item i = (Item) i1;
                    if (i.getAmbiente().getId() == a.getId()) {
                        valora = valora + i.getValor_compra();
                    }
                }
                jTextField3.setText("R$" + String.format("%.2f", valora));
            }
        } else {
            jButton1.setEnabled(false);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
        }
    }//GEN-LAST:event_jList1ValueChanged

