    private void scboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scboxActionPerformed
        // TODO add your handling code here:
        if (scbox.getSelectedIndex() != 0) {
            acbox.setEnabled(true);
            acbox.removeAllItems();
            Servidor s = (Servidor) scbox.getSelectedItem();
            List<Object> Ambiente = ADAO.listar();
            for (Object a : Ambiente) {
                Ambiente a1 = (Ambiente) a;
                if (a1.getServidor().getId() == s.getId()) {
                    acbox.addItem(a);
                }
            }

        }


    }//GEN-LAST:event_scboxActionPerformed

