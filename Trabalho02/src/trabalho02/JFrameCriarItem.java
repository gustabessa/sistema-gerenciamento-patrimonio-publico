    private void aconfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aconfirmActionPerformed
        // TODO add your handling code here:
        Item i = new Item();
        i.setEspecificacao(iespecificacao.getText());
        Ambiente a = (Ambiente) acbox.getSelectedItem();
        i.setAmbiente(a);
        a.setData_modificacao(LocalDate.now());
        ADAO.alterar(a);
        Servidor s = (Servidor) scbox.getSelectedItem();
        i.setDono(s);
        i.setData_compra(LocalDate.parse(idatacompra.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        i.setCod_partimonio(Integer.parseInt(icodpatrimonio.getText()));
        i.setData_criacao(LocalDate.now());
        i.setData_modificacao(LocalDate.now());
        i.setEstado(Integer.parseInt(iestado.getText()));
        i.setValor_compra(Double.parseDouble(ivalorcompra.getText()));
        i = (Item) IDAO.adiciona(i);

        this.dispose();
    }//GEN-LAST:event_aconfirmActionPerformed

