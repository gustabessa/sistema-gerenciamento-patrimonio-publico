    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        MovimentacaoDono md = new MovimentacaoDono();
        md.setData_criacao(LocalDate.now());
        md.setData_modificacao(LocalDate.now());
        Ambiente a = (Ambiente) ambiente.getSelectedItem();
        md.setAmbiente(a);
        md.setMotivo(motivo.getText());
        md.setOrigem(a.getServidor());
        a.setData_modificacao(LocalDate.now());
        Servidor s = (Servidor) servidor.getSelectedItem();
        a.setServidor(s);
        md.setDestino(s);
        ADAO.alterar(a);
        for(Object i1 : Item) {
            Item i = (Item) i1;
            if(i.getAmbiente().getId() == a.getId()) {
                i.setDono(s);
                i.setData_modificacao(LocalDate.now());
                IDAO.alterar(i);
            }
        }
        MDDAO.adiciona(md);
        tmodel.add(md);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

