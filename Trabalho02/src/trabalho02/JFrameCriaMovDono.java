    private void campusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_campusActionPerformed
        // TODO add your handling code here:
        if(campus.hasFocus()) {
            if(campus.getSelectedIndex() != 0) {
                Campus c = (Campus) campus.getSelectedItem();
                int tam = servidor.getItemCount()-1;
                int tam1 = ambiente.getItemCount()-1;
                for(int i = 0; i < tam; i++)
                    servidor.removeItemAt(1);
                for(int i = 0; i < tam1; i++)
                    ambiente.removeItemAt(1);
                for(Object o : Ambiente) {
                    Ambiente a = (Ambiente) o;
                    if(a.getCampus().getId() == c.getId())
                    ambiente.addItem(o);
                }
                for(Object o : Servidor) {
                    Servidor s = (Servidor) o;
                    if(s.getCampus().getId() == c.getId())
                    servidor.addItem(o);
                }
            }
        }
    }//GEN-LAST:event_campusActionPerformed

