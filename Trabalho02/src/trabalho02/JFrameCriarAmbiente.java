    private void ccboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ccboxActionPerformed
        // TODO add your handling code here:
        if (ccbox.hasFocus()) {
            Campus c1 = (Campus) ccbox.getSelectedItem();
            int tam = scbox.getItemCount() - 1;
            for (int i = 0; i < tam; i++) {
                scbox.removeItemAt(1);
            }
            for (Object s1 : Servidor) {
                Servidor s = (Servidor) s1;
                if (s.getCampus().getId() == c1.getId()) {
                    scbox.addItem(s);
                }
            }
        }
    }//GEN-LAST:event_ccboxActionPerformed

