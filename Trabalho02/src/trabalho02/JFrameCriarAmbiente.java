    private void aconfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aconfirmActionPerformed
        // TODO add your handling code here:
        Ambiente a1 = new Ambiente();
        a1.setDescricao(adescricao.getText());
        Campus c = (Campus) ccbox.getSelectedItem();
        a1.setCampus(c);
        Servidor s = (Servidor) scbox.getSelectedItem();
        a1.setServidor(s);
        a1.setData_criacao(LocalDate.parse(adatacriacao.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        a1.setData_modificacao(a1.getData_criacao());
        a1 = (Ambiente) ADAO.adiciona(a1);
        model1.addElement(a1.toString());

        this.dispose();
    }//GEN-LAST:event_aconfirmActionPerformed

